def calculate_volume_of_sphere(radius):
		pi = 3.14
		area = pi * 4 / 3 * radii ** 3
		return area

radii1 = 30
area1 = calculate_volume_of_sphere(radii1)
print(f"The area of sphere with radii {radii1} is: {area1}")

radii2 = 40
area2 = calculate_volume_of_sphere(radii2)
print(f"The area of circle with radii {radii2} is: {area2}")
